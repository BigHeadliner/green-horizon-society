$(function () {   
     
    
    document.querySelectorAll('.news__item').forEach(function(item) {
        item.addEventListener('mouseover', function() {
          this.classList.add('rotate');
        });
      
        item.addEventListener('mouseout', function() {
          this.classList.remove('rotate');
        });
      });
      

  
 
  
 
  
  
  
  
     

 



  


  
  
   
});